public class pelayanan {
    
    private String idPel,namaPel;
    private int hargaPel;

   

    /**
     * @return the idPel
     */
    public String getIdPel() {
        return idPel;
    }

    /**
     * @param idPel the idPel to set
     */
    public void setIdPel(String idPel) {
        this.idPel = idPel;
    }

    /**
     * @return the namaPel
     */
    public String getNamaPel() {
        return namaPel;
    }

    /**
     * @param namaPel the namaPel to set
     */
    public void setNamaPel(String namaPel) {
        this.namaPel = namaPel;
    }

    /**
     * @return the hargaPel
     */
    public int getHargaPel() {
        return hargaPel;
    }

    /**
     * @param hargaPel the hargaPel to set
     */
    public void setHargaPel(int hargaPel) {
        this.hargaPel = hargaPel;
    }
    
}
